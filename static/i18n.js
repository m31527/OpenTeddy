  // Resolve _extends chains into flat merged objects
  function resolveLocale(code) {
    const raw = MSGS[code] || {};
    if (raw._extends) {
      const parent = resolveLocale(raw._extends);
      const { _extends: _, ...rest } = raw;
      return Object.assign({}, parent, rest);
    }
    return raw;
  }
  const RESOLVED = {};
  Object.keys(MSGS).forEach(c => { RESOLVED[c] = resolveLocale(c); });

  // Active locale — default en-US; override from localStorage
  let _locale = localStorage.getItem('teddy.locale') || 'en-US';
  if (!MSGS[_locale]) _locale = 'en-US';

  function t(key, params) {
    const dict = RESOLVED[_locale] || RESOLVED['en-US'];
    const base = RESOLVED['en-US'];
    let str = dict[key] !== undefined ? dict[key] : (base[key] !== undefined ? base[key] : key);
    if (params) {
      Object.keys(params).forEach(k => {
        str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
      });
    }
    return str;
  }

  function setLocale(code) {
    if (!MSGS[code]) return;
    _locale = code;
    localStorage.setItem('teddy.locale', code);
    applyI18n();
    // Dispatch so the app can react (e.g. re-render dynamic content)
    document.dispatchEvent(new CustomEvent('teddy:locale-changed', { detail: { locale: code } }));
  }

  function getLocale() { return _locale; }

  function applyI18n() {
    // textContent
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      el.textContent = t(key);
    });
    // placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    // title
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = t(el.getAttribute('data-i18n-title'));
    });
    // aria-label
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
    });
    // HTML content (for elements that contain markup + text)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      el.innerHTML = t(el.getAttribute('data-i18n-html'));
    });
  }

